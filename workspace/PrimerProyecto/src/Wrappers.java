public class Wrappers {
    //Clase que ayuda a verificar los valores máximos y mínimos
	//para los tipos de datos primitivos numéricos

	public static void main(String[] args) {
		System.out.println("Byte.BYTES: " + Byte.BYTES);//1
		System.out.println("Byte.MAX_VALUE: " + Byte.MAX_VALUE);//127
		System.out.println("Byte.MIN_VALUE: " + Byte.MIN_VALUE);//-128
		System.out.println("Byte.SIZE: " + Byte.SIZE);//8
		
		System.out.println("Short.BYTES: " + Short.BYTES);
		System.out.println("Short.MAX_VALUE: " + Short.MAX_VALUE);
		System.out.println("Short.MIN_VALUE: " + Short.MIN_VALUE);
		System.out.println("Short.SIZE: " + Short.SIZE);
		
		System.out.println("Integer.BYTES: " + Integer.BYTES);
		System.out.println("Integer.MAX_VALUE: " + Integer.MAX_VALUE);
		System.out.println("Integer.MIN_VALUE: " + Integer.MIN_VALUE);
		System.out.println("Integer.SIZE: " + Integer.SIZE);
		
		System.out.println("Long.BYTES: " + Long.BYTES);
		System.out.println("Long.MAX_VALUE: " + Long.MAX_VALUE);
		System.out.println("Long.MIN_VALUE: " + Long.MIN_VALUE);
		System.out.println("Long.SIZE: " + Long.SIZE);
		
		System.out.println("Float.BYTES: " + Float.BYTES);
		System.out.println("Float.MAX_VALUE: " + Float.MAX_VALUE);
		System.out.println("Float.MIN_VALUE: " + Float.MIN_VALUE);
		System.out.println("Float.SIZE: " + Float.SIZE);
		
		System.out.println("Double.BYTES: " + Double.BYTES);
		System.out.println("Double.MAX_VALUE: " + Double.MAX_VALUE);
		System.out.println("Double.MIN_VALUE: " + Double.MIN_VALUE);
		System.out.println("Double.SIZE: " + Double.SIZE);

	}

}//class Wrappers
