
public class ForDemo {

	public static void main(String[] args) {

		for(int cont = 5; cont <= 100; cont+=5) {
			System.out.println(cont);
		}//for
		System.out.println("===============");
		for(int cont = 100; cont >= 5; cont-=5) {
			System.out.println(cont);
		}//for
		

	}//main

}//class ForDemo
